/*
Problem : You went to the supermarket to buy some oranges and apples. Calculate how much money the shopkeeper will return.

Output:
Print the result.

Example Input:
Gave customer: 1000
Total Price: 700

Example Output:
Return: 300
*/

// Solution :

let apples = 400;
let oranges = 300;
let totalPrice = apples + oranges;
console.log(totalPrice);

let customer = 1000;
let moneyBack = customer - totalPrice;
